open()

#Modos da função "open()" do python
# a: Modo de anexar, adiciona informações ao final do arquivo
# a+: Modo de anexar e leitura, adiciona informações ao final do arquivo
# r: Modo de leitura, o arquivo deve existir previamente
# r+: Modo de leitura e escrita, o arquivo deve existir previamente
# w: Modo de escrita, se o arquivo não exitir, ele será criado
# w+: Modo de escrita e leitura, se o arquivo não existir ele será criado
# x: Modo exclusivo, cria um novo arquivo somente se ele não existir
# x+: Modo exclusivo e leitura, cria um novo arquivo somente se ele não existir