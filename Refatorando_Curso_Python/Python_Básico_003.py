#Operações aritméticas em linguagem Python.
print('Primeiro resultado:', 5 + 3)
print('Segundo resultado:', 5 - 3)
print('Terceiro resultado:', 5 * 3)
print('Quarto resultado:', 5 / 3)
print('Qunito resultado:', 5 ** 3)
print('Sexto resultado:', 5 // 3)
print('Sétimo resultado:', 5 % 3)

#Funções Aritmétcas em linguagem Python.
print('\nFunção abs():', abs(-3))
print('Função pow():', pow(5, 3))
print('Função max():', max(5, 4, 3, 2, 1))
print('Função min():', min(5, 4, 3, 2, 1))
print('Função round():', round(5,3))
